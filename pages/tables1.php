// Veritabanı bağlantısı
$conn = new mysqli('localhost', 'root', '', 'futbol_scouting');

// Bağlantı hatası kontrolü
if ($conn->connect_error) {
    die("Bağlantı hatası: " . $conn->connect_error);
}

// Pozisyonlar ve Türkçe karşılıkları
$positions = [
    1 => "Kaleci",
    2 => "Stoper",
    3 => "Sol Bek",
    4 => "Sağ Bek",
    5 => "Defansif Orta Saha",
    6 => "Merkez Orta Saha",
    7 => "Ofansif Orta Saha",
    8 => "Sağ Kanat",
    9 => "Sol Kanat",
    10 => "Santrafor"
];

// Seçili pozisyon
$selectedPosition = isset($_GET['position']) ? (int)$_GET['position'] : null;

// Oyuncuları, takımları, ligleri ve ülke bilgilerini çekme sorgusu
$sqlPlayers = "
    SELECT 
        p.player_id, 
        p.player_name, 
        GROUP_CONCAT(pp.position_id SEPARATOR ',') AS position_ids,
        t.team_name,
        IFNULL(l.league_name, 'Lig Bilinmiyor') AS league_name,
        IFNULL(l.country, 'Ülke Bilinmiyor') AS country
    FROM players p
    JOIN playerpositions pp ON p.player_id = pp.player_id
    LEFT JOIN teams t ON p.team_id = t.team_id
    LEFT JOIN leagues l ON t.league_id = l.league_id
";

if ($selectedPosition) {
    $sqlPlayers .= " WHERE pp.position_id = $selectedPosition";
}
$sqlPlayers .= " GROUP BY p.player_id, p.player_name, t.team_name, l.league_name, l.country";

$resultPlayers = $conn->query($sqlPlayers);

// Oyuncu verilerini kaydet
$players = [];
if ($resultPlayers && $resultPlayers->num_rows > 0) {
    while ($row = $resultPlayers->fetch_assoc()) {
        $row['position_names'] = array_map(function ($id) use ($positions) {
            return $positions[$id] ?? "Bilinmeyen";
        }, explode(',', $row['position_ids']));
        $players[] = $row;
    }
}

// Veritabanı bağlantısını kapat
$conn->close();
